package main.patterns;

public class Book {
    int bshifr;
    String name;
    String genre;

    public int getBshifr() {
        return bshifr;
    }

    public void setBshifr(int bshifr) {
        this.bshifr = bshifr;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }
}
